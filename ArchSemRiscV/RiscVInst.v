(** The semantics of instructions from [sail-riscv] by using the conversion code
    from [ArchSem.FromSail]. *)
Definition sail_riscv_sem (nondet : bool) : iMon () :=
  iMon_from_Sail nondet (rv64d.try_step 0 true);; mret ().
